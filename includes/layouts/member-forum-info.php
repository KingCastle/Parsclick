<h1><i class="fa fa-comments fa-lg"></i> انجمن، سوالات و نظرات</h1>
<p><em class="text-success">برای آنکه نظر، سوال و دیدکاهتان را مطرح کنید، لطفا به این نکات توجه کنید.</em></p>
<p class="lead text-danger"><i class="fa fa-info-circle"></i> نکات مهم:</p>
<ul>
	<li>از تگ های <code>&lt;strong&gt;&lt;em&gt;&lt;i&gt;&lt;p&gt;&lt;code&gt;&lt;pre&gt;&lt;mark&gt;&lt;span&gt;&lt;ul&gt;&lt;ol&gt;&lt;li&gt;&lt;a&gt;&lt;img&gt;</code>
	    می توانید استفاده کنید.
	</li>
	<li>برای چپ چین کردن از سی اس اس کلاس <code>edit</code> استفاده کنید.</li>
	<li>برای انداختن کلمات انگلیسی بین کلمات فارسی از تگ <code>&lt;code&gt;</code> استفاده کنید.</li>
	<li>دور کد های چند خطه از تگ <code>&lt;pre&gt;</code> استفاده کنید</li>
	<li>اگر لیست اضافه می کنید از تگ های زیر استفاده کنید:
						<pre class="line-numbers"><code class="language-html">&lt;ul&gt;<br>&lt;li&gt;لیست اول&lt;/li&gt;
&lt;li&gt;لیست دوم&lt;/li&gt;&lt;/ul&gt;</code></pre>
	</li>
	<li>تگ های <code>HTML</code> قابل رویت نیست یعنی وقتی مطلبی رو می سازید که می خواهید کدهای
		<code>HTML</code> رو نشون بدید این کدها دیده نخواهند شد چون کدهای <code>HTML</code> تبدیل میشوند. برای
	    نشون دادن این کدها باید <code>&lt;&gt;</code> ها را به این صورت تبدیل کنید:
								<pre class="line-numbers"><code class="language-html">&lt;html&gt; => &amp;lt;html&amp;gt;
&lt;/html&gt; => &amp;lt;/html&amp;gt;</code></pre>
	</li>
	<li>برای رنگ گرفتن کد های شما ما از <a href="http://prismjs.com/" title="prism" target="_blank">prism
		</a> استفاده میکنیم. استفاده از <code>prism</code> اینطور هست که کدهاتون رو داخل تگ <code>pre</code> و
	    بعد داخل تگ <code>code</code> قرار میدهید و به تگ کد کلاس <code>language-{html}</code> میدهید و به تگ
		<code>pre</code> کلاس <code>line-numbers</code> می دهید. توجه داشته باشید که به جای <code>{html}</code>
	    می تونید از هر زبانی دیگر استفاده کنید. مثال زیر مثالی هست که معمولا ما از آن استفاده می کنیم
								<pre class="line-numbers"><code class="language-html">&lt;pre class="line-numbers"&gt;&lt;code
class="language-php"&gt; کدهای شما &lt;/code&gt;&lt;/pre&gt;</code></pre>
	</li>
	<li>سوالات مربوط به هر درس را در انجمن مخصوص به همون درس مطرح کنید. اگر سوال نامرتبط به درس یا سوال مرطبت به درس دیگری داخل درس نامرتبط مطرح کنید، سوال جواب داده نخواهد شد.</li>
</ul>