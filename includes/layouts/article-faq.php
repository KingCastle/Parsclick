	<p class="text-danger" id="1">۱. آیا آموزش این درس را خواهی داد؟</p>
	<p class="text-success">اگر درخواست درسی را کردید حتما به آن رسیدگی خواهد شد و در مورد آن درس ویدئو خواهم ساخت.</p>
	<p class="text-danger" id="2">۲. این کد را می نویسم اما اجرا نمی شود چکار کنم؟</p>
	<p class="text-success">اگر مشکلاتی در کد نویسی و استفاده از برنامه دارید سعی نمایید ویدئو ها را با دقت دنبال
	                        نمایید.</p>
	<p class="text-danger" id="3">۳. چطور می توانم با شما همکاری کنم؟</p>
	<p class="text-success">در مورد همکاری با ما لطفا CV خود را به آدرس ایمیل ما بفرستید.</p>
	<p class="text-danger" id="4">۴. چرا ویدئوهایت را داخل سایت های ایرانی آپلود نمی کنی؟</p>
	<p class="text-success">دلیل از آپلود نکردن روی سرورهای ایرانی: کم سرعت بودن، کم بودن مخاطب، نا امن بودن، پایین بودن
	                        کیفیت، نگه داری پایین، پایین بودن پیدایش موتور جستجو و خیلی از دلایلی هست که به اختصار قادر
	                        به توضیح نیستم اما در حال حاضر سرورهای ایرانی برای کار ما کفایت نمی کنند.</p>
	<p class="text-danger" id="5">۵. آیا می توانم ویدئوهایت را دانلود کنم؟</p>
	<p class="text-success">از دانلود کردن ویدئوهایم برای کسانی که دسترسی به یوتوب ندارند ناراحت نمی شوم اما این کار را
	                        درست ندانسته و باعث کندی درسهایی که خود شما خواسته اید خواهد شد.</p>
	<p class="text-danger" id="6">۶. چطور این برنامه را نصب کنم؟</p>
	<p class="text-success">سوالات در مورد نصب برنامه جواب داده نخواهد شد به سه دلیل: برنامه توسط ما ساخته نشده، نحوه
	                        دانلود برنامه توسط شما برای ما قابل اثبات نیست و آموزشهای ما به استفاده از برنامه بعد از نصب
	                        برنامه بر میگردد.</p>
	<p class="text-danger" id="7">۷. می خواهم برنامه نویسی را شروع کنم اما نمی دانم از کجا؟</p>
	<p class="text-success">کسانی که می خواهند برنامه نویسی را شروع کنند ولی نمی دانند از کجا باید شروع کنند باید بدانند
	                        که برنامه نویسی به خیلی شاخه های متعدد تقسیم می شود و انتخاب از آن شاخه ها به عهده ی خودتان
	                        است.</p>
	<p class="text-danger" id="8">۸. مشکل خصوصی دارم و به کمک شما احتیاج دارم؟</p>
	<p class="text-success">مشکلات خصوصی قابل حل با ایمیل نیست. مشکلات خصوصی جواب داده نمی شود. لطفا مشکلات مربوط به درس
	                        را مطرح کنید.</p>
	<p class="text-danger" id="9">۹. در مورد Networking ویدئو خواهی گذاشت؟</p>
	<p class="text-success">در مورد Networking ویدئو خواهم گذاشت اما این ویدئو ها مقدماتی خواهد بود نه پیشرفته دلیلش این
	                        هست که بنده مهندس نرم افزار هستم نه مهندس نت ورک اما در مورد آن حتما صحبت خواهم کرد. فعلا در
	                        مورد Networking ویدئو نخواهم گذاشت!</p>
	<p class="text-danger" id="10">۱۰. چطور می توانم به شما کمک کنم؟</p>
	<p class="text-success">دوستانی که قصد کمک دارند لطف می کنند اگر به قسمت کمک به مای وب سایت مراجعه کنند.</p>
	<p class="text-danger" id="11">۱۱. امکان پست کردن ویدئوها برای شما مقدور است؟</p>
	<p class="text-success">بر خلاف خیلی از سایت هایی که برای بدردنخورترین ویدئوها درخواست پول میکنند, پارس کلیک برای
	                        هیچ کدام از ویدئوهایش درخواست پول نمیکند. باور کنید اگر می شد تمام ویدئوها را رایگان به درب
	                        خانه ی شما می فرستادم اما این کار هزینه ی خیلی بالایی دارد. با توجه به اینکه ما داخل ایران
	                        نیستیم و باز از مشکلات فیلترینگ در ایران آگاه هستیم. همانطور که گفته شد قصد ما آموزش رایگان
	                        برای تمام مردم هست. پیشنهاد استفاده از فیلتر شکن را میکنیم و قول میدهیم به زودی وب هاست
	                        خودمان را تهیه و ویدئوهایمان را رایگان آنجا هاست کنیم. اما باور کنید فعلا هیچ وب سایتی برای
	                        آپلود ویدئوها در جهان بهتر از YouTube نیست. به وب سایت هایی مثل تخت سفید و آپارات هیچ
	                        اطمینانی نیست. آنها خیلی کند هستند و قابلیت مرتب سازی، SEO و خیلی چیزهایی را که ما به عنوان
	                        برنامه نویس می دانیم را ندارند. وب سایت Vimeo هم برای آپلود مقدار زیادی ویدئو و فضای باز از
	                        ما پول می خواهد. پس ما لطف می کنیم آموزش درست می کنیم، وقت میگذاریم، وب سایت درست می کنیم و
	                        به فکر شما هستیم و از شما این درخواست کوچک را داریم که به YouTube دسترسی پیدا کنید، برای
	                        خودتان، آموزش ها را نگاه کنید. اما اگر این کار برای شما مقدور نیست خیلی متاسفیم.</p>
