	<h2>جواب سوالاتی که ممکنه بپرسید</h2>
	<p>ایمیلهای شما که از طریق تماس با مای وب سایت می فرستید خوانده می شود و در اسرع وقت جواب داده خواهد شد. دلیل سرعت
	   پایین جواب دادن به ایمیل ها این هست که اولا تعداد آنها بسیار زیاد می باشد و دوما با گذاشتن تمام وقت ما به ساختن
	   ویدئو ها، ویرایش آنها، آپلود کردن آنها و بسیاری دیگر از مسائل شخصی دیگر وقتی بسیار برای پاسخگویی باقی نمی ماند.
	   تعداد اندکی از ایمیل ها را شخص بنده و بسیاری دیگر را همکارم خوانده اند و پاسخ خواهند داد. ایمیلهایی که بنده
	   خواندم و بسیاری در مورد مشکلات شما دوستان بود باید بگویم که در ویدئو ها
	   جواب داده خواهد شد.</p>
