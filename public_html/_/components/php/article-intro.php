	<h2>فرم نویسندگی</h2>
	<p>بنابراین فرمی رو برای نویسندگی قرار دادم که از دوستانی که علاقمند به نویسندگی هستند و به ما ایمیل می زنند درخواست
	   کردیم این فرم رو پر کنند و برای ما بفرستند.</p>
	<p>لطفا توجه داشته باشید که برای پر کردن این فرم ابتدا اون رو ذخیره کنید و بعد پر کنید و به آدرس ایمیل داخل فرم بفرستید. </p>
