	<h2>درباره سایت</h2>
	<p>چند سال پیش با یک ایده شروع شد. وقتی داشتم تو اینترنت دنبال کتابخانه ی ویدیویی فارسی زبان می گشتم. اسم یک زبان
	   برنامه نویسی رو به فارسی و یا حتی به فینگلیش تایپ می کردم. اما تا چشم کار می کرد با مقاله ها و یا ویدیوهای نصفه و
	   نیمه کاره میامد بالا. ولی چیزی که به دنبالش بودم کتابخانه ی ویدیویی بود. کتابخانه ای رایگان اما با ویدیوهایی با
	   کیفیت بالا. نبود! یک مقدار تحقیق که کردیم خوردیم به سرعت اینترنت فوق العاده کم که برای هموطنان وجود داشت، فیلترینگ
	   و مسایلی که می بایست دلایل باشند که باعث شده بود اینکار نشه. یک میکروفن ساده، یک لپ تاپ، یک ذره اراده و جایی خلوت
	   که بشه اینکار رو شدنی کرد. اون موقع اینقدر ذوق و شوق داشتم که دوست داشتم همه چیز رو ویدیو کنم. شروع کردم ار محصولات
	   آدوبی. اما زیاد به دلم ننشست بخودم گفتم چرا در مورد تخصص خودم ویدیو نسازم. همیشه این سوال برام پیش میومد، چطور شروع
	   کنم، ایده رو از کی بگیرم، بگذار ببینم کتابخانه های ویدیویی بزرگ دیگه چکار کردند که من هم همون کار رو کنم. خلاصه
	   پارس کلیک با هدف رفع نیازهای کشور درست شد، حتی اگر می دیدم می تونستم دست یک عده ی کمی رو هم بگیرم می گیرم. ولی
	   اینقدر دوستان فهمیده در این مسیر به پستم خورد که من رو همیشه به این کار تشویق می کرد. چند بار به خاطر عوض شدن زندگی
	   می خواستم دست بردارم اما همیشه یک چیزی جلوم رو گرفت. باید ابزاری می ساختم که اگر شده نقطه ی شروعی هم برای کسی باشه،
	   بشه. شما ها هم کمک کردید که ما زنده بمونیم.</p>
